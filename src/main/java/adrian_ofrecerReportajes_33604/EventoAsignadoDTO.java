package adrian_ofrecerReportajes_33604;

public class EventoAsignadoDTO {
    private String id;
    private String nombreEvento;
    private String fecha;
    private String nombreReportero;

    public EventoAsignadoDTO() {}

    public String getId() { return id; }
    public void setId(String id) { this.id = id; }
    public String getNombreEvento() { return nombreEvento; }
    public void setNombreEvento(String nombreEvento) { this.nombreEvento = nombreEvento; }
    public String getFecha() { return fecha; }
    public void setFecha(String fecha) { this.fecha = fecha; }
    public String getNombreReportero() { return nombreReportero; }
    public void setNombreReportero(String nombreReportero) { this.nombreReportero = nombreReportero; }
}